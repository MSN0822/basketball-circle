def thin_border():
    s = Side(style='thin', color='CCCCCC')
    return Border(left=s, right=s, top=s, bottom=s)

def make_sheet(wb, name, tab_color):
    ws = wb.create_sheet(name)
    ws.sheet_properties.tabColor = tab_color
    return ws

def write_header(ws, headers, header_fill, col_widths):
    hfont = Font(name='Meiryo UI', bold=True, size=10)
    for col, (h, w) in enumerate(zip(headers, col_widths), 1):
        c = ws.cell(row=1, column=col, value=h)
        c.font = hfont
        c.fill = header_fill
        c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
        c.border = thin_border()
        ws.column_dimensions[get_column_letter(col)].width = w
    ws.row_dimensions[1].height = 28

def write_rows(ws, data_rows, biko_fill, note_col=5):
    dfont = Font(name='Meiryo UI', size=9)
    for i, r in enumerate(data_rows):
        row_num = i + 2
        for col, val in enumerate(r, 1):
            c = ws.cell(row=row_num, column=col, value=val)
            c.font = dfont
            c.alignment = Alignment(vertical='top', wrap_text=True)
            c.border = thin_border()
            if col == 1:
                c.alignment = Alignment(horizontal='center', vertical='top')
            if col == note_col and val:
                c.fill = biko_fill
        ws.row_dimensions[row_num].height = 52
    ws.freeze_panes = 'A2'

